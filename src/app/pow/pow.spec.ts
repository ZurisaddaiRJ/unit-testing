import { pow } from './pow';

describe('pow', () =>{
    it('should return 1 if exponent is 0', () =>{
        const result = pow();
        expect(result).toBe();
    })

    it('should return 0 if exponent is negative', () =>{
        const result = pow();
        expect(result).toBe();
    })

    it('should return 8 if base is 2 and exponent is 3', () =>{
        const result = pow();
        expect(result).toBe();
    })

    it('should return 9 if base is 3 and exponent is 2', () =>{
        const result = pow();
        expect(result).toBe();
    })
})